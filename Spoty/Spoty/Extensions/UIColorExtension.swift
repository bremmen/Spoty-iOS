import UIKit

extension UIColor {
    static var greenSpoty: UIColor {
        return UIColor.init(named: "Green")!
    }

    static var blackSpoty: UIColor {
        return UIColor.init(named: "Black")!
    }

    static var greySpoty: UIColor {
        return UIColor.init(named: "Grey")!
    }
}
